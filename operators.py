# Arithmetic operator
x=20
y=10
print("\n 20+10=",x+y)
print("\n 20-10=",x-y)
print("\n 20*10=",x*y)
print("\n 20%10=",x%y)
print("\n 20/10=",x/y)
print("\n 20**10=",pow(x,y))

#comparison operators
print("x>y",x>y)
print("x<y",x<y)
print("x==y",x==y)
print("x!=y",x!=y)

#logical operator
print("x and y is",x and y)
print("x or y is",x or y)
print("x not y is", not y)